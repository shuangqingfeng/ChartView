class Car {
    private float minute;
    private String minuteDesc;
    private String desc;

    public float getMinute() {
        return minute;
    }

    public void setMinute(float minute) {
        this.minute = minute;
    }

    public String getMinuteDesc() {
        return minuteDesc;
    }

    public void setMinuteDesc(String minuteDesc) {
        this.minuteDesc = minuteDesc;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }
}
